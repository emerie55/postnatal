$(document).ready(function(){
    
    $('.toggleBtn').on('click', function(){
        $('#mainNav').toggleClass('open');
        $('#navToggle').toggleClass('toggleBtnPos');
    });

    $('select[name=category]').on('change', function(){
        $value = $(this).val();
        if($value==1 || $value==2){
            $('#moreDet').slideDown();
        }else{
            $('#moreDet').slideUp();
        








































































































































































        }
    });
});
